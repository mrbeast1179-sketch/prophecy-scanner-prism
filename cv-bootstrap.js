  function parseRow(line) {
    var cells = line.split(',').map(function (c) { return c.trim(); });
    var row = {
      symbol: cells[0],
      exp: cells[1],
      strike: Number(cells[2]),
      type: cells[3],
      iv: Number(cells[4]),
      delta: Number(cells[5]),
      gamma: Number(cells[6]),
      theta: Number(cells[7]),
      vega: Number(cells[8]),
      bid: Number(cells[9]),
      ask: Number(cells[10]),
      mid: Number(cells[11]),
      OI: Number(cells[12]),
      vol: Number(cells[13]),
      spot: Number(cells[14])
    };
    return row;
  }

