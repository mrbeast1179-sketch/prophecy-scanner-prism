  // Project one row into the IDX-ordered 14-tuple layout index.html
  // expects. Mirrors cv-bootstrap.demo.js's makeSide() exactly.
  function sideTuple(r) {
    return [
      r.exp,        // 0  EXP
      r.strike,     // 1  STRIKE
      r.type,       // 2  TYPE  ('C' or 'P')
      r.iv,         // 3  IV
      r.delta,      // 4  DELTA
      r.gamma,      // 5  GAMMA
      r.theta,      // 6  THETA
      r.vega,       // 7  VEGA
      r.bid,        // 8  BID
      r.ask,        // 9  ASK
      r.mid,        // 10 MID
      r.OI,         // 11 OI
      r.vol,        // 12 VOL
      r.spot        // 13 SPOT
    ];
  }

  function buildChain(symbol) {
    var rows = ROWS.filter(function (r) { return r.symbol === symbol; });
    var byExp = {};
    rows.forEach(function (r) {
      byExp[r.exp] = byExp[r.exp] || {};
      var byStrike = byExp[r.exp];
      byStrike[r.strike] = byStrike[r.strike] || {};
      byStrike[r.strike][r.type] = r;
    });
    var exps = Object.keys(byExp).sort();
    var chain = exps.map(function (exp) {
      var byStrike = byExp[exp];
      var strikes = Object.keys(byStrike)
        .map(function (k) { return Number(k); })
        .sort(function (a, b) { return a - b; });
      return {
        expiration: exp,
        strikes: strikes.map(function (K) {
          var pair = [K];
          if (byStrike[K].C) pair.push(sideTuple(byStrike[K].C));
          if (byStrike[K].P) pair.push(sideTuple(byStrike[K].P));
          return pair;
        })
      };
    });
    return { symbol: symbol, chain: chain };
  }

  // OPRA-style synthetic contract ticker: SPY260731C00580
  function contractTickerOf(r) {
    var yymmdd = r.exp.replace(/-/g, '').slice(2); // 2026-07-31 -> 260731
    var code = r.type === 'C' ? 'C' : 'P';
    var strike = String(r.strike).padStart(5, '0');
    return r.symbol + yymmdd + code + strike;
  }

  function buildScreen(query) {
    var cols = (query && query.columns) || [
      'underlying_ticker', 'ticker', 'contract_type', 'strike_price',
      'expiration_date', 'day_volume', 'open_interest',
      'implied_volatility', 'delta', 'underlying_price'
    ];
    var limit = (query && query.limit) || 300;
    var sortKey = (query && query.sort && query.sort[0] && query.sort[0].field) || 'day_volume';
    var sortDir = (query && query.sort && query.sort[0] && query.sort[0].direction) || 'desc';
    var colIdx = function (name) {
      for (var i = 0; i < cols.length; i++) if (cols[i] === name) return i;
      return cols.length - 1;
    };
    var rows = ROWS.map(function (r) {
      return [
        r.symbol,                  // 0 underlying_ticker
        contractTickerOf(r),       // 1 ticker
        r.type === 'C' ? 'call' : 'put', // 2 contract_type
        r.strike,                  // 3 strike_price
        r.exp,                     // 4 expiration_date
        r.vol,                     // 5 day_volume
        r.OI,                      // 6 open_interest
        r.iv,                      // 7 implied_volatility
        r.delta,                   // 8 delta
        r.spot                     // 9 underlying_price
      ];
    });
    var k = colIdx(sortKey);
    var dir = sortDir === 'desc' ? -1 : 1;
    rows.sort(function (a, b) {
      var av = a[k], bv = b[k];
      if (typeof av === 'string') return dir * bv.localeCompare(av);
      return dir * (bv - av);
    });
    return { columns: cols, rows: rows.slice(0, limit) };
  }

